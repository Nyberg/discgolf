    <h4>Spelide</h4>

    <p>
Discgolfens spelidé är densamma som för vanlig slaggolf: att på så få kast som möjligt klara av en bana med ett antal hål. Men istället för att svinga en klubba kastar man en disc och hålen man ska träffa består av en metallställning med en rund korg och kedjor som hänger ovanför korgen. Kedjorna bromsar upp discen och låter den sedan falla ner i korgen.</p>

    <h4>Bana</h4>
<p>
En bana består vanligtvis 9,12 eller 18 hål. Hålens längd varierar från 65m till 300m. Hållängder som är långt ifrån minigolf alltså men också något kortare än slaggolf. Från två till fem kast brukar vara idealresultat på hålen, beroende på längd och svårighet. Idealresultatet för ett hål kallas "par". I Sverige finns ca. 70 banor, från Ystad till Luleå, vanligtvis i park- eller skogsmiljö. De allra flesta banor är gratis att spela på men ett fåtal betalbanor finns också. Den första betalbanan i Sverige och kanske också den mest kända är Järva Disc Golf Park i Stockholm. I norra Sverige finns Terminalen i Skellefteå.
</p><p><a href="/course" class="red">Hitta din bana på Penguin Project</a></p>
