<h4>Varför heter det inte Frisbeegolf?</h4>

<p>Därför att föremålet man kastar inte har så mycket gemensamt med mack-frisbees eller liknande för parken och stranden utan har en utpräglad disc-karaktär: tunga, små och en låg profil.
</p><hr/>
<h4>Mer om spelet</h4>

<p>Utkast görs från en bestämd plats som avgränsas av en utkastplanka. Ansatsens underlag är vanligtvis betong eller grus. När man skall göra sitt andra kast markerar man sitt läge med en minidisc i framkanten av utkastdiscens placering sett i riktning mot hålet.
</p>
<p>Det är alltid den som är längst ifrån korgen som börjar kasta. De andra spelarna skall då helst befinna sig bakom den som skall kasta.

Vid nästa hål utgår kastordningen från föregående håls resultat och den som har lägst resultat börjar kasta.</p>
