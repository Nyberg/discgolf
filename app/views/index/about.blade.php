<h4>Spontangolf</h4>

<p>Gör upp en bana i park/lekplats/skogsmiljö med markerade utkast, markerade med spray eller naturliga utkast som ”vid trädet”, ”bakom skylten” osv. Är utrymmmet begränsat kasta med frisbees, eller minidiscar, istället för golfdiscar eftersom de inte går lika långt. Använd bestämda mål som att träffa en lyktstolpe eller en trädstam. Även alternativa måltavlor är kul, tex. att kasta in i ett fotbollsmål eller under en skylt eller landa i en sandlåda. En bana kan förslagsvis bestå av 3, 6 eller 9 hål. För att variera hålen kan man använda sig av dog-leg, vilket innebär tex. att man måste runda ett visst träd innan man kastar mot målet. Använd räknekort för att notera hur många kast varje deltagare får på resepektive hål.
</p>

